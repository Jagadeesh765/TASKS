# Python Collections – List, Nested List, Set, Dictionary, and Tuple

# 1. List
# A list is an ordered and mutable (changeable) collection that can store different data types and duplicate values.
# Syntax:
# list_name = [item1, item2, item3]
# Example:
# fruits = ["Apple", "Banana", "Mango"]
# print(fruits)

# 2. Nested List
# A nested list is a list that contains one or more lists as its elements. It is used to store multi-dimensional data.
# Syntax:
# nested_list = [[1, 2], [3, 4]]
# Example:
# marks = [[90, 85], [88, 92]]
# print(marks[1][0])   # Output: 88

# 3. Set
# A set is an unordered collection of unique elements. Duplicate values are not allowed.
# Syntax:
# set_name = {item1, item2, item3}
# Example:
# numbers = {10, 20, 30, 20}
# print(numbers)   # Output: {10, 20, 30}

# 4. Dictionary
# A dictionary stores data in key-value pairs. Keys are unique and values can be duplicated.
# Syntax:
# dict_name = {"key": "value"}
# Example:
# student = {"Name": "John", "Age": 20}
# print(student["Name"])

# 5. Tuple
# A tuple is an ordered and immutable (unchangeable) collection. It allows duplicate values.
# Syntax:
# tuple_name = (item1, item2, item3)
# Example:
# colors = ("Red", "Green", "Blue")
# print(colors[0])